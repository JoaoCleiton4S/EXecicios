public class menorque {
    public static void main(String[] args) {

        int number = -1;

        if (number >= 1) {
            System.out.printf("O número %d é positivo", number);
        } else if (number < 0) {
            System.out.printf("O número %d é negativo", number);
        } else {
            System.out.println("O número é zero");
        }

    }
}
